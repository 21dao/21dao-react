export function marketplaceLink(source, mint, brand_name, username) {
  if (source === "holaplex") {
    return `https://${brand_name}.holaplex.com/listings/${mint}`;
  }
  if (source === "exchange") {
    return `https://exchange.art/single/${mint}`;
  }
  if (source === "formfunction") {
    return `https://formfunction.xyz/@${username}/${mint}`;
  }
}

export function sellerLink(source, brand) {
  if (source === "holaplex") {
    return `https://${brand}.holaplex.com`;
  }
  if (source === "exchange") {
    return `https://exchange.art/search?from=30&filters={"tokenStatus":["curated","certified","known"],"brands":["${brand}"]`;
  }
  if (source === "formfunction") {
    return `https://formfunction.xyz/@${brand}`;
  }
}

export function buyerLink(highest_bidder) {
  return `https://looksrare.so/l/${highest_bidder}`;
}

